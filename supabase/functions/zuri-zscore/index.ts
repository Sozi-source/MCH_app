// ─── LMS Z-score calculation ───────────────────────────────────────────────────

function calcZ(value: number, L: number, M: number, S: number): number {
  let z: number;
  if (Math.abs(L) < 1e-6) {
    z = Math.log(value / M) / S;
  } else {
    z = (Math.pow(value / M, L) - 1) / (L * S);
  }
  // WHO convention: clamp to ±6
  return Math.max(-6, Math.min(6, z));
}

/**
 * Interpolate LMS values from a table at a given key (age in months or height in cm).
 * Uses linear interpolation between the two nearest rows.
 */
function interpolateLMS(
  table: [number, number, number, number][],
  key: number,
): { L: number; M: number; S: number } | null {
  if (table.length === 0) return null;

  // Clamp to table bounds
  if (key <= table[0][0]) return { L: table[0][1], M: table[0][2], S: table[0][3] };
  if (key >= table[table.length - 1][0]) {
    const last = table[table.length - 1];
    return { L: last[1], M: last[2], S: last[3] };
  }

  // Find bracketing rows
  let lo = 0;
  let hi = table.length - 1;
  while (hi - lo > 1) {
    const mid = Math.floor((lo + hi) / 2);
    if (table[mid][0] <= key) lo = mid; else hi = mid;
  }

  const [k0, L0, M0, S0] = table[lo];
  const [k1, L1, M1, S1] = table[hi];
  const t = (key - k0) / (k1 - k0);

  return {
    L: L0 + t * (L1 - L0),
    M: M0 + t * (M1 - M0),
    S: S0 + t * (S1 - S0),
  };
}

